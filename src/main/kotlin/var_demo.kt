/********************************************* 变量/常量 ******************************************************/
/*
 * 定义
 * 1.全局变量/常量(直接定义在kt文件中/定义在类中)必须声明同时初始化
 * 2.局部变量可以先声明，后初始化
 * 3.声明同时初始化可以不指定类型，由编译器自动识别；先声明后初始化的必须指定类型
 */
// 变量
var a = 1
var b: String = ""
// 常量
val c = "1"
val d: Float = 1.4f

fun definition() {
    var a: Int
    var b: String
}